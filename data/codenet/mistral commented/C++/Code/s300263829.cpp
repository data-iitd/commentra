
#include<iostream> // Include the iostream library for input and output operations
using namespace std; // Use the standard namespace for easier access to iostream functions

int main() // Declare the main function
{
  int a, b; // Declare two integer variables a and b
  cin >> a >> b; // Read two integers from the standard input and assign them to variables a and b
  cout << a * b << endl; // Multiply the values of variables a and b and print the result to the standard output
  return 0; // Indicate that the program has run successfully
}