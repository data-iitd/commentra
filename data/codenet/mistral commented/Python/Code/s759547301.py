
S = input('Please enter a string: ') # Take an input string S from the user

if S == 'Sunny': # Check if the string S is equal to 'Sunny'
    print('Cloudy') # If yes, print 'Cloudy'

if S == 'Cloudy': # Check if the string S is equal to 'Cloudy'
    print('Rainy') # If yes, print 'Rainy'

if S == 'Rainy': # Check if the string S is equal to 'Rainy'
    print('Sunny') # If yes, print 'Sunny'

# If none of the conditions are met, the program ends here.