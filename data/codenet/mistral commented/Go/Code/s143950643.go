
package main

import (
	"fmt" // Importing the 'fmt' package for input/output operations
)

func main() {
	// Declaring a variable 'a' of type 'int'
	var a int

	// Reading an integer value from the standard input using 'fmt.Scan'
	fmt.Scan(&a)

	// Multiplying the value of 'a' with itself twice and storing the result in the 'fmt.Println' function
	fmt.Println(3 * a * a)
	// This block prints the square of the number 'a' multiplied by 3
}
