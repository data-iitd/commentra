
a,b = map(int,input().split())

# Taking two integer inputs from the user
# and storing them in variables `a` and `b`.

if a <= b:
    # If `a` is less than or equal to `b`, print the value of `a`.
    print(a)
else:
    # If `a` is greater than `b`, print the value of `a` minus 1.
    print(a-1)

