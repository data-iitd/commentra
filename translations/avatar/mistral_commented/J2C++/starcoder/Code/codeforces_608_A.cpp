
#include <bits/stdc++.h>

using namespace std;

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    int n;
    cin >> n;

    while (n--) {
        int f, t;
        cin >> f >> t;

        int max = 0;

        if (max < f + t) {
            max = f + t;
        }
    }

    cout << max << endl;

    return 0;
}

