

package main

import (
	"bufio"
	"fmt"
	"os"
)

const MOD = 100000007

func main() {
	reader := bufio.NewReader(os.Stdin)
	n, k := readTwoNums(reader)
	coef := make([]int, n)
	for i := 1; i < int(n*1.0); i++ {
		coef[i-1] = n / i - n/(i+1)
	}
	coef = append(coef, 1)
	for i := len(coef) - 1; i > 0; i-- {
		coef[i-1] += coef[i]
	}
	dp := make([][]int, k+1)
	for i := 0; i <= k; i++ {
		dp[i] = make([]int, n)
	}
	dp[0][0] = 1
	for i := 1; i <= k; i++ {
		var prev int
		for j := n - 1; j >= 0; j-- {
			prev += dp[i-1][j]
			if prev >= MOD {
				prev -= MOD
			}
			dp[i][j] = int(int64(coef[j])*prev%MOD)
		}
	}
	var res int
	for i := 0; i < n; i++ {
		res += dp[k][i]
		if res >= MOD {
			res -= MOD
		}
	}
	fmt.Println(res)
}

