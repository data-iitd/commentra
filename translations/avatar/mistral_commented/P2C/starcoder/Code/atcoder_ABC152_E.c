int sum(int A[], int N)
{
    // Initializing the variable sum to 0.
    int sum = 0;
    // Iterating through each number a in the list A and updating the value of sum.
    for (int i = 0; i < N; i++)
        sum += A[i];
    // Returning the value of sum.
    return sum;
}

