// Function to read N lines of input and return them as a list of tuples
vector<vector<int>> Line(int N) {
    vector<vector<int>> read_all;
    for (int i = 0; i < N; i++) {
        vector<int> temp;
        string s = input();
        for (int j = 0; j < s.length(); j++) {
            temp.push_back(s[j] - '0');
        }
        read_all.push_back(temp);
    }
    return read_all;
}

