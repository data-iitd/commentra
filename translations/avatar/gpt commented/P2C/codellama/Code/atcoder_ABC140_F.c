int* LI() {
    int* S = malloc(sizeof(int) * I());
    for (int i = 0; i < I(); i++) {
        scanf("%d", &S[i]);
    }
    return S;
}

// Function to sort a list of integers in descending order
void sort(int* S, int N) {
    for (int i = 0; i < N - 1; i++) {
        for (int j = i + 1; j < N; j++) {
            if (S[i] < S[j]) {
                int temp = S[i];
                S[i] = S[j];
                S[j] = temp;
            }
        }
    }
}

