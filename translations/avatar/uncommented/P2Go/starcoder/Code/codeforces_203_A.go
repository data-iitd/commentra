func main() {
    reader := bufio.NewReader(os.Stdin)
    fmt.Println("Enter the values of x,t,a,b,da,db")
    x, t, a, b, da, db := readLine(reader)
    if x == 0 {
        fmt.Println("YES")
        return
    }
    a_time := min(a/da, t-1)
    b_time := min(b/db, t-1)
    for i := 0; i <= a_time; i++ {
        for j := 0; j <= b_time; j++ {
            if a-da*i == x || b-db*j == x {
                fmt.Println("YES")
                return
            }
            if a-da*i+b-db*j == x {
                fmt.Println("YES")
                return
            }
        }
    }
    fmt.Println("NO")
}
