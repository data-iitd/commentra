template<typename T> T gcd(T a, T b) {
    if(!b) return a;
    return gcd(b, a % b);
}
template<typename T> T lcm(T a, T b) {
    return a * b / gcd(a, b);
}

using namespace std;

typedef long long ll;
typedef long double ld;

int K;

int solve() {
    if (K % 2 == 0 || K % 5 == 0) return -1;
    if (K % 7 == 0) K /= 7;
    K *= 9;
    int ans = 1;
    int remainder = 10 % K;
    while (remainder != 1) {
        ans++;
        remainder = remainder * 10 % K;
    }
    return ans;
}

int main(void) {
    scanf("%d", &K);
    printf("%d\n", solve());
    return 0;
}

