
package main

import (
	"bufio"
	"fmt"
	"io"
	"os"
	"strconv"
	"strings"
)

var (
	in  = bufio.NewReader(os.Stdin)
	out = bufio.NewWriter(os.Stdout)
)

func main() {
	S := readString()
	score := 0
	for i := 0; i < len(S); i++ {
		my := 'g'
		if i%2 == 0 {
			my = 'p'
		}
		his := S[i]
		if my!= his {
			if my == 'p' {
				score++
			} else {
				score--
			}
		}
	}
	fmt.Println(score)
}

func readString() string {
	s, _ := in.ReadString('\n')
	s = strings.TrimSpace(s)
	return s
}

func readInt() int {
	i, _ := strconv.Atoi(readString())
	return i
}

func readInt64() int64 {
	i, _ := strconv.ParseInt(readString(), 10, 64)
	return i
}

func readFloat64() float64 {
	f, _ := strconv.ParseFloat(readString(), 64)
	return f
}

func readLine() {
	_, _ = in.ReadString('\n')
}

