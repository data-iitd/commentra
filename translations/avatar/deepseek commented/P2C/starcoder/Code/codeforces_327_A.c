int* find(char* s, char ch)
{
    int* indices = (int*)malloc(sizeof(int) * (strlen(s) + 1));
    int i = 0, j = 0;
    while (s[i]!= '\0')
    {
        if (s[i] == ch)
        {
            indices[j] = i;
            j++;
        }
        i++;
    }
    indices[j] = -1;
    return indices;
}

