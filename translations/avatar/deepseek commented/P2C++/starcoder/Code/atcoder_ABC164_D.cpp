
#include <iostream>
#include <string>
#include <vector>

int main ( ) {
    std::string s;
    int l = 2019;
    std::vector<int> m ( l + 1, 0 );
    int a = 0;
    int r = 0;
    std::cin >> s;
    for ( int i = 0 ; i < s.length ( ) ; i++ ) {
        a += ( int ) ( s [ i ] - '0' ) * pow ( 10, s.length ( ) - i - 1, l );
        r += m [ a % l ];
        m [ a % l ] += 1;
    }
    std::cout << r << std::endl;
    return 0;
}
