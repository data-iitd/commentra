import sys

# Main function: This is the entry point of the program.
def main():
    s = input()  # Reading the input string from the console.
    if 'A' in s and 'B' in s:  # Checking if the string contains both 'A' and 'B'.
        print("Yes")  # Printing "Yes" if the condition is true.
    else:
        print("No")  # Printing "No" if the condition is false.

if __name__ == "__main__":
    main()  # Calling the main function to execute the program.

