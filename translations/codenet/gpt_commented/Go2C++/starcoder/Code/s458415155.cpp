// Computes the minimum cost to reach the last height using dynamic programming.
int solve(int n, vector<int> &heights) {
	vector<int> t(n + 1, numeric_limits<int>::max());
	t[0] = 0;
	for (int i = 1; i < n; i++) {
		// Calculate the cost to reach the current height from the previous height.
		int v = t[i - 1] + abs(heights[i] - heights[i - 1]);
		if (v < t[i]) {
			t[i] = v;
		}
		// Calculate the cost to reach the current height from two heights back.
		if (1 < i) {
			v = t[i - 2] + abs(heights[i] - heights[i - 2]);
			if (v < t[i]) {
				t[i] = v;
			}
		}
	}
	return t[n - 1];
}

// END-OF-CODE

int main() {
	int n;
	cin >> n;
	vector<int> heights(n);
	for (int i = 0; i < n; i++) {
		cin >> heights[i];
	}
	cout << solve(n, heights) << endl;
	return 0;
}

