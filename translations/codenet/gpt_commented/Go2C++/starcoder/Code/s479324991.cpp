#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int main() {
	// Initialize a new scanner to read input
	Scanner sc;

	// Read the number of elements
	int n = sc.NextInt();
	// Create a vector to hold the float64 values
	vector<double> arr(n);
	
	// Read n integers from input and convert them to float64
	for (int i = 0; i < n; i++) {
		arr[i] = sc.NextInt();
	}
	
	// Sort the vector of float64 values
	sort(arr.begin(), arr.end());

	// Initialize 'prev' with the first element of the sorted array
	double prev = arr[0];
	
	// Calculate the average of the elements iteratively
	for (int i = 1; i < n; i++) {
		prev = (prev + arr[i]) / 2;
	}

	// Print the final computed average
	cout << prev << endl;
}

