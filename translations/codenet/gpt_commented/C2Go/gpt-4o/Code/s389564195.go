package main

import (
    "fmt"
)

func main() {
    // Declare an integer variable x for input
    var x int

    // Prompt the user to enter an integer value
    fmt.Scan(&x)

    // Calculate the cube of the input value x
    kotae := x * x * x

    // Output the result (the cube of x) to the console
    fmt.Println(kotae)
}

