#include <stdio.h> // Include the standard input/output library

int main() {
    int r; // Declare an integer variable 'r' to store user input
    scanf("%d", &r); // Read an integer value from standard input and store it in 'r'
    
    // Calculate the square of 'r' and print the result
    printf("%d\n", r * r); // Output the square of 'r' followed by a newline

    return 0; // Return 0 to indicate successful completion
}

