
# Read the first integer from user input
a = int(input())

# Read the second integer from user input
b = int(input())

# Check if the product of a and b is even
if (a * b) % 2 == 0:
    # If the product is even, print "Even"
    print("Even")
else:
    # If the product is odd, print "Odd"
    print("Odd")

