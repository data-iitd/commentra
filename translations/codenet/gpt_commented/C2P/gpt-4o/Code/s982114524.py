# Declare variables
x = 0  # Initialize x to 0 to hold the sum

# Read an integer N from user input
N = int(input())

# Loop from 1 to N to calculate the sum of integers
for i in range(1, N + 1):
    x += i  # Add the current value of i to x

# Print the calculated sum
print(x)

