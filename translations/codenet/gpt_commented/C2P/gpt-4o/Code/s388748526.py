import math  # Import math library for mathematical constants and functions

# Prompt the user to input the radius
r = float(input("Enter the radius: "))  # Read the radius from user input

# Calculate and print the area and circumference of the circle
# Area = π * r^2
# Circumference = 2 * π * r
print(r * r * math.pi, 2 * math.pi * r)

