
import java.util.Scanner; // Import the Scanner class from the java.util package

public class s845656342{ // Define a class named s845656342
  public static void main(String[] args) { // Define the main method
    Scanner scanner = new Scanner(System.in); // Create a Scanner object to read user input
    int x = scanner.nextInt(); // Read an integer value from user input and store it in x
    if(x == 1) { // Check if the value of x is equal to 1
      System.out.println("0"); // If x is 1, print "0"
    } else {
      System.out.println("1"); // If x is not 1, print "1"
    }
  }
}
