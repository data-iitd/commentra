import java.util.*; // Import the Java utilities library

public class Main {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); // Create a Scanner object to read input from stdin
    int r = sc.nextInt(); // Read an integer value from stdin and store it in 'r'
    System.out.println(r*r); // Output the square of 'r' followed by a newline
  }
}

