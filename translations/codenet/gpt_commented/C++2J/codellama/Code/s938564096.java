import java.util.Scanner; // Import the Scanner class for input operations

public class Main {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); // Create a new Scanner object for input
    int r = sc.nextInt(); // Read an integer value from standard input and store it in 'r'
    System.out.println(r*r); // Output the square of 'r' followed by a newline
  }
}

