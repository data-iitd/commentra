

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// reva reverses a slice of integers in place.
void reva(int *b, int n) {
	for (int i = 0; i < n/2; i++) { // Iterate only halfway through the slice
		// Swap the elements at positions i and n-1-i
		int t = b[i];
		b[i] = b[n-1-i];
		b[n-1-i] = t;
	}
}

int main(int argc, char *argv[]) {
	int n, q, b, e;
	scanf("%d", &n);
	int *a = malloc(n * sizeof(int));
	for (int i = 0; i < n; i++) {
		scanf("%d", &a[i]);
	}
	scanf("%d", &q);
	for (int i = 0; i < q; i++) {
		scanf("%d %d", &b, &e);
		reva(a+b, e-b);
	}
	for (int i = 0; i < n; i++) {
		if (i > 0) {
			printf(" ");
		}
		printf("%d", a[i]);
	}
	printf("\n");
	free(a);
	return 0;
}

