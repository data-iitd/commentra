

#include <stdio.h>
#include <string.h>
#include <stdlib.h>

#define MOD 1000000000 + 7
#define ALPHABET_NUM 26
#define INF_INT64 9223372036854775807
#define INF_BIT60 18446744073709551616

int memo[ALPHABET_NUM];
char str[1000000];

int main() {
	scanf("%s", str);

	// Count occurrences of each letter.
	for (int i = 0; i < strlen(str); i++) {
		char c = str[i];

		if ('a' <= c && c <= 'z') {
			// Increment count for lowercase letters.
			memo[c-'a']++;
		} else if ('A' <= c && c <= 'Z') {
			// Increment count for uppercase letters.
			memo[c-'A']++;
		} else {
			// Ignore non-alphabet characters.
			continue;
		}
	}

	// Print the count of each letter.
	for (int i = 0; i < ALPHABET_NUM; i++) {
		printf("%c : %d\n", i+'a', memo[i]);
	}
}

