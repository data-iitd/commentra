

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

// Function to return the maximum of two integers
int max(int a, int b) {
	if (a < b) {
		return b;
	}
	return a;
}

// Function to calculate the number of full groups needed to accommodate N items
int main() {
	// Read three integers from input: H, W, and N
	int H, W, N;
	scanf("%d %d %d", &H, &W, &N);

	// Calculate the maximum of H and W
	int A = max(H, W);

	// Calculate and print the number of full groups needed to accommodate N items
	printf("%d\n", (N + A - 1) / A);
}

