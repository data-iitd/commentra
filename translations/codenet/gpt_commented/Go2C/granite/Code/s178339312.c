
#include <stdio.h>

int main() {
    // Declare a variable 'x' of type int to store user input
	int x; 

    // Read an integer value from standard input and store it in 'x'
	scanf("%d", &x);

    // Calculate the cube of 'x' and print the result to standard output
	printf("%d\n", x*x*x);

    // End of code
	return 0;
}
