#include<iostream> // Include the standard input/output library

using namespace std; // Use the standard namespace

int main() // The entry point of the program
{
	int a, b; // Declare two integer variables 'a' and 'b'

	cin >> a >> b; // Read two integers from the standard input and store them in variables 'a' and 'b'

	cout << ((b-1)+(a-2))/(a-1) << endl; // Print the result of the expression ((b-1)+(a-2))/(a-1) to the standard output

	return 0; // Indicate that the program has executed successfully
}

