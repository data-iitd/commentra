

a, b, c = map(int, input().split()) # Read the values of a, b, and c from the standard input using input() and split()

if a + b >= c: # Check if the sum of a and b is greater than or equal to c
  print("Yes") # If the condition is true, print the string "Yes" to the standard output
else:
  print("No") # If the condition is false, print the string "No" to the standard output

