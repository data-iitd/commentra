# Main function is the entry point of the program
def main():
    # Declare and initialize two integer variables, N and A
    N = int(input())  # Read the value of N from the standard input
    A = int(input())  # Read the value of A from the standard input

    # Calculate the result and print it to the standard output
    print(N * N - A)

if __name__ == "__main__":
    main()

