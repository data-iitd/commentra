
# Declare a string variable named S
S = input()

# Check if the string is equal to "Sunny"
if S == "Sunny":
    # If true, print "Cloudy"
    print("Cloudy")
# Check if the string is equal to "Cloudy"
elif S == "Cloudy":
    # If true, print "Rainy"
    print("Rainy")
# If neither of the conditions are true
else:
    # Print "Sunny"
    print("Sunny")

