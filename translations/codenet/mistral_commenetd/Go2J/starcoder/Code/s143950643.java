
package com.company;

import java.util.Scanner;

public class Main {

    public static void main(String[] args) {
        // Creating a 'Scanner' object to read input from the standard input
        Scanner input = new Scanner(System.in);

        // Declaring a variable 'a' of type 'int'
        int a;

        // Reading an integer value from the standard input using 'input.nextInt()'
        a = input.nextInt();

        // Multiplying the value of 'a' with itself twice and storing the result in the 'System.out.println()' function
        System.out.println(3 * a * a);
        // This block prints the square of the number 'a' multiplied by 3
    }
}

