

import java.util.Scanner; // Importing the 'Scanner' class from the 'java.util' package for input/output operations

public class s143950643{
    public static void main(String[] args) {
        // Declaring a variable 'a' of type 'int'
        int a;

        // Creating a 'Scanner' object to read input from the standard input
        Scanner scanner = new Scanner(System.in);

        // Reading an integer value from the standard input using'scanner.nextInt()' and storing it in the variable 'a'
        a = scanner.nextInt();

        // Multiplying the value of 'a' with itself twice and printing the result using 'System.out.println'
        System.out.println(3 * a * a);
        // This block prints the square of the number 'a' multiplied by 3
    }
}

