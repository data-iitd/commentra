
# Package main is the entry point of the program
def main():
  # Declare a variable named S of type string
  S = input()

  # Switch statement to check the value of the string S and print the corresponding weather condition
  if S == "Sunny":
    print("Cloudy")
  elif S == "Cloudy":
    print("Rainy")
  elif S == "Rainy":
    print("Sunny")

# End of the code
