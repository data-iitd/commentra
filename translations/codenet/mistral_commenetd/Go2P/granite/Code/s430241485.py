
import sys # Import the "sys" module for accessing system-specific parameters and functions

# Define the main function, entry point of our program
def main():
    # Use the "input" function to read input from the standard input (keyboard)
    # and store it in variables s and t respectively
    s = input()
    t = input()

    # Print the concatenated string of t and s using the "print" function
    print(t + s)

# Call the main function to execute the program
if __name__ == "__main__":
    main()

