# Declare a variable named day
day = int(input())

# Use a conditional statement to determine the name of the day based on the given day number
if day == 25:
    # If the day is 25, print "Christmas"
    print("Christmas")
elif day == 24:
    # If the day is 24, print "Christmas Eve"
    print("Christmas Eve")
elif day == 23:
    # If the day is 23, print "Christmas Eve Eve"
    print("Christmas Eve Eve")
elif day == 22:
    # If the day is 22, print "Christmas Eve Eve Eve"
    print("Christmas Eve Eve Eve")

