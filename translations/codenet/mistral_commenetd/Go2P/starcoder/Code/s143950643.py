
# Importing the'sys' module for input/output operations
import sys

# Declaring a variable 'a' of type 'int'
a = 0

# Reading an integer value from the standard input using'sys.stdin.readline'
a = int(sys.stdin.readline())

# Multiplying the value of 'a' with itself twice and storing the result in the 'print' function
print(3 * a * a)
# This block prints the square of the number 'a' multiplied by 3

