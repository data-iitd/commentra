
#include<stdio.h> // Include the standard input/output library for input and output operations

int main() // Declare the main function
{
  int a, b; // Declare two integer variables a and b
  scanf("%d %d", &a, &b); // Read two integers from the standard input and assign them to variables a and b
  printf("%d\n", a * b); // Multiply the values of variables a and b and print the result to the standard output
  return 0; // Indicate that the program has run successfully
}

