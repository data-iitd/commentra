# The main function is the starting point of the program
def main():
    # Reading an integer value from the standard input and storing it in the variable x
    x = int(input())
    
    # If the value of x is 0, then print the value 1 followed by a newline character
    if x == 0:
        print("1")
    # If the value of x is 1, then print the value 0 followed by a newline character
    if x == 1:
        print("0")

# Calling the main function to execute the program
if __name__ == "__main__":
    main()

