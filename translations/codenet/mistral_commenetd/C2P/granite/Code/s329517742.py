

a, b = map(int, input().split()) # Reading two integers from the standard input and storing them in variables a and b

if 500*a >= b: # Logical block to check if 500 times the value of variable 'a' is greater than or equal to the value of variable 'b'
	print("Yes") # If the condition is true, print 'Yes' to the standard output
else:
	print("No") # If the condition is false, print 'No' to the standard output

