# Include the standard input/output library
# Include the standard library for memory allocation
# Include the math library for square root calculation

# The main function is the entry point of the program
def main():
  x = int(input())  # Read an integer value from the standard input and store it in the variable x

  if x == 1:  # Check if the value of x is equal to 1
    print("0")  # If x is 1, print 0 to the standard output
  else:  # If x is not equal to 1
    print("1")  # Otherwise, print 1 to the standard output

# Indicate that the program has run successfully and terminate
if __name__ == "__main__":
  main()

