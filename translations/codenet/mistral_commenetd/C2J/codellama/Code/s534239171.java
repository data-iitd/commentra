
public class Average {

 public static void main(String[] args) {

 int a, b, n;

 Scanner sc = new Scanner(System.in);

 System.out.print("Enter two integers: ");

 a = sc.nextInt();

 b = sc.nextInt();

 n = (a + b) / 2;

 System.out.println("The average of " + a + " and " + b + " is: " + n);

 }

}

