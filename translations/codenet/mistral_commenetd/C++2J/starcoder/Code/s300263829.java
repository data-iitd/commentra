
import java.util.*; // Include the Scanner class for reading input from the standard input

public class Main {
  public static void main(String[] args) {
    Scanner input = new Scanner(System.in); // Create a Scanner object for reading input from the standard input
    int a = input.nextInt(); // Read an integer from the standard input and assign it to variable a
    int b = input.nextInt(); // Read an integer from the standard input and assign it to variable b
    System.out.println(a * b); // Multiply the values of variables a and b and print the result to the standard output
  }
}

