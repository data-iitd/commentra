
import java.util.Scanner; // Import the Scanner class for input operations

public class Main {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); // Create a Scanner object to read input from the standard input
    int a = sc.nextInt(); // Read an integer from the standard input and assign it to variable a
    int b = sc.nextInt(); // Read an integer from the standard input and assign it to variable b
    System.out.println(a * b); // Multiply the values of variables a and b and print the result to the standard output
  }
}
