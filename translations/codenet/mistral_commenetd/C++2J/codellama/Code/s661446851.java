import java.util.*;
import java.lang.*;
import java.io.*;

class Main {
    public static void main(String[] args) {
        // Code for standard input and output
        Scanner sc = new Scanner(System.in);
        PrintWriter pw = new PrintWriter(System.out);
        // Code for scanning input and printing output
        int n = sc.nextInt();
        if (n <= 0) {
            pw.println("0");
        }
        pw.close();
    }
}

