
import java.util.Scanner; // Import the Scanner class for reading input from the standard input

public class Main {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); // Create a new Scanner object for reading input from the standard input
    int r = sc.nextInt(); // Read an integer `r` from the standard input
    System.out.println(r*r); // Print the square of `r` to the standard output
  }
}

