public class CubeCalculator {
    public static void main(String[] args) {
        java.util.Scanner scanner = new java.util.Scanner(System.in);
        // Create a Scanner object to read input from the user.
        int x = scanner.nextInt();
        // Use the nextInt method of the Scanner object to read an integer input from the user and assign it to the variable x.
        System.out.println(x * x * x);
        // Use the println method of the System.out object to display the result of the computation x*x*x on the console.
    }
}
