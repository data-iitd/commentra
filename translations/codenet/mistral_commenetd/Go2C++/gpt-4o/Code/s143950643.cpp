#include <iostream> // Including the iostream library for input/output operations

int main() {
    // Declaring a variable 'a' of type 'int'
    int a;

    // Reading an integer value from the standard input using 'std::cin'
    std::cin >> a;

    // Multiplying the value of 'a' with itself twice and printing the result
    std::cout << 3 * a * a << std::endl;
    // This block prints the square of the number 'a' multiplied by 3

    return 0; // Returning 0 to indicate successful execution
}

