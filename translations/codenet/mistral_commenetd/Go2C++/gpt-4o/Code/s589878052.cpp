// Including required header files
#include <iostream>

using namespace std;

// Main function
int main() {
    // Variable declaration
    int a, b, h;

    // Reading input values from the standard input
    cin >> a >> b >> h;

    // Calculating the result
    int result = (a + b) * h / 2;

    // Printing the result to the standard output
    cout << result << endl;

    return 0;
}

