// Function to calculate the maximum, minimum, sum, and absolute difference of integers
int Max(int a[], int n) {
	int r = a[0];
	for (int i = 0; i < n; i++) {
		if (r < a[i]) {
			r = a[i];
		}
	}
	return r;
}

int Min(int a[], int n) {
	int r = a[0];
	for (int i = 0; i < n; i++) {
		if (r > a[i]) {
			r = a[i];
		}
	}
	return r;
}

int Sum(int a[], int n) {
	int r = 0;
	for (int i = 0; i < n; i++) {
		r += a[i];
	}
	return r;
}

int Abs(int a) {
	if (a < 0) {
		return -a;
	}
	return a;
}

