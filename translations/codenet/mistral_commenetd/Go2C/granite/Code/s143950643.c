

#include <stdio.h> // Importing the'stdio.h' header file for input/output operations

int main() {
	// Declaring a variable 'a' of type 'int'
	int a;

	// Reading an integer value from the standard input using'scanf'
	scanf("%d", &a);

	// Multiplying the value of 'a' with itself twice and storing the result in the 'printf' function
	printf("%d\n", 3 * a * a);
	// This block prints the square of the number 'a' multiplied by 3

	return 0; // Returning 0 to indicate successful execution of the program
}

