#include <stdio.h>
#include <string.h>

int main() {
	char s[100];
	printf("Enter a string: ");
	scanf("%s", s);

	// Replace all occurrences of the string "2017" in the string `s` with the string "2018".
	str_replace(s, "2017", "2018");

	// Print the modified string to the standard output.
	printf("Modified string: %s\n", s);
}

// End of code.

// Define a function `str_replace` to replace all occurrences of the string "2017" in the string `s` with the string "2018".
void str_replace(char s[], char old[], char new[]) {
	int i, j, k;
	for (i = 0; s[i]!= '\0'; i++) {
		for (j = 0; old[j]!= '\0'; j++) {
			if (s[i] == old[j]) {
				for (k = i; s[k]!= '\0'; k++) {
					s[k] = new[j];
				}
				break;
			}
		}
	}
}

