
#include <stdio.h>
#include <stdlib.h>
#include <math.h>

// Initialize IO object and handle resources
int main() {
	// Initialize variables for heights and minimum difference
	int n, minIdx;
	double t, a, minDiff;

	// Read input values
	scanf("%d %lf %lf", &n, &t, &a);

	// Initialize variables for heights and minimum difference
	double heights[n];
	minDiff = abs(t - heights[0] * 0.006 - a);
	minIdx = 0;

	// Iterate through the remaining heights and find the minimum difference
	for (int i = 1; i < n; i++) {
		// Calculate the difference between the target and the height
		double diff = abs(t - heights[i] * 0.006 - a);

		// Update minimum difference and index if the current difference is smaller
		if (diff < minDiff) {
			minDiff = diff;
			minIdx = i;
		}
	}

	// Print the result
	printf("%d\n", minIdx + 1);

	// Handle resources
	return 0;
}

