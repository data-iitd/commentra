// Import the standard input/output library
#include <stdio.h>

int main() {
    // Declare a variable named x of type int
    int x;

    // Read an integer value from the standard input and assign it to the variable x
    scanf("%d", &x);

    // Print the result of x cubed (x*x*x)
    printf("%d\n", x * x * x);

    return 0;
}

