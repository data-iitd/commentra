#include <stdio.h> // Including the standard input/output library

int main() {
    // Declaring a variable 'a' of type 'int'
    int a;

    // Reading an integer value from the standard input using 'scanf'
    scanf("%d", &a);

    // Printing the result of 3 times the square of 'a'
    printf("%d\n", 3 * a * a);
    // This block prints the square of the number 'a' multiplied by 3

    return 0; // Returning 0 to indicate successful execution
}
