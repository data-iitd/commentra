#include <stdio.h>

int main() {
  int a, b;

  // Reading input values for variables 'a' and 'b' from the standard input
  scanf("%d %d", &a, &b);

  // Multiplying the variables 'a' and 'b' and printing the result
  printf("%d\n", a * b);

  // Multiplying the sum of 'a' and 'b' with 2 and printing the result
  printf("%d\n", (a + b) * 2);

  return 0;
}
