
package main

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	// Read input
	reader := bufio.NewReader(os.Stdin)
	n, m := readTwoNums(reader)

	// Initialize dynamic programming table with infinite values
	dp := make([][]int, (1<<12)+5)
	for i := 0; i < (1<<12)+5; i++ {
		dp[i] = make([]int, 1005)
		for j := 0; j < 1005; j++ {
			dp[i][j] = 100000000
		}
	}
	dp[0][0] = 0

	// Read bulbs' information
	key := make([]int, m+1)
	cost := make([]int, m+1)
	for i := 1; i <= m; i++ {
		cost[i], _ = reader.ReadBytes('\n')
		x, _ := reader.ReadBytes('\n')
		b := 0
		for _, num := range x {
			b |= 1 << (int(num)-1)
		}
		key[i] = b
	}

	// Fill dynamic programming table
	for i := 0; i < (1<<n); i++ {
		for j := 0; j < m; j++ {
			dp[i][j+1] = min(dp[i][j], dp[i][j+1])
			dp[i|key[j+1]][j+1] = min(dp[i|key[j+1]][j+1], dp[i][j]+cost[j+1])
		}
	}

	// Output result
	if dp[(1<<n)-1][m] == 100000000 {
		fmt.Println(-1)
	} else {
		fmt.Println(dp[(1<<n)-1][m])
	}
}

func readTwoNums(reader *bufio.Reader) (a int, b int) {
	res := readNNums(reader, 2)
	a, b = res[0], res[1]
	return
}

func readNNums(reader *bufio.Reader, n int) []int {
	res := make([]int, n)
	x := 0
	bs, _ := reader.ReadBytes('\n')
	for i := 0; i < n; i++ {
		for x < len(bs) && (bs[x] < '0' || bs[x] > '9') && bs[x]!= '-' {
			x++
		}
		x = readInt(bs, x, &res[i])
	}
	return res
}

func readUint64(bytes []byte, from int, val *uint64) int {
	i := from

	var tmp uint64
	for i < len(bytes) && bytes[i] >= '0' && bytes[i] <= '9' {
		tmp = tmp*10 + uint64(bytes[i]-'0')
		i++
	}
	*val = tmp

	return i
}

func readInt(bytes []byte, from int, val *int) int {
	i := from
	sign := 1
	if bytes[i] == '-' {
		sign = -1
		i++
	}
	tmp := 0
	for i < len(bytes) && bytes[i] >= '0' && bytes[i] <= '9' {
		tmp = tmp*10 + int(bytes[i]-'0')
		i++
	}
	*val = tmp * sign
	return i
}

func readString(reader *bufio.Reader) string {
	s, _ := reader.ReadString('\n')
	for i := 0; i < len(s); i++ {
		if s[i] == '\n' {
			return s[:i]
		}
	}
	return s
}

