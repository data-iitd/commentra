#include <stdio.h>  # Step 1: Include necessary headers
#include <stdlib.h> # for standard library functions
#include <math.h>   # for mathematical functions

def main(): # Step 2: Define the main function
  x = int(input()) # Step 3: Declare an integer variable
  if x == 1: # Step 4: Read an integer value from the user
    print(0) # Print 0 if the input is 1
  else:
    print(1) # Print 1 if the input is not 1

  return 0 # Step 5: Return 0 to indicate successful execution

