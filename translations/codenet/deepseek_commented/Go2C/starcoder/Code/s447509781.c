typedef struct {
  char *buffer;
  size_t size;
  size_t length;
} Input;

void initInput(Input *input, FILE *file) {
  input->buffer = NULL;
  input->size = 0;
  input->length = 0;
  fseek(file,0,SEEK_END);
  input->size = ftell(file);
  fseek(file,0,SEEK_SET);
  input->buffer = malloc(input->size+1);
  fread(input->buffer,1,input->size,file);
  input->buffer[input->size] = 0;
}

void freeInput(Input *input) {
  free(input->buffer);
}

char *readLine(Input *input) {
  char *buffer = input->buffer;
  size_t size = input->size;
  size_t length = input->length;
  char *line = NULL;
  if (length == size) {
    size *= 2;
    buffer = realloc(buffer,size+1);
    input->buffer = buffer;
    input->size = size;
  }
  line = buffer + length;
  while (1) {
    if (length == size) {
      size *= 2;
      buffer = realloc(buffer,size+1);
      input->buffer = buffer;
      input->size = size;
    }
    char c = fgetc(stdin);
    if (c == '\n') {
      buffer[length] = 0;
      input->length = length+1;
      return line;
    }
    buffer[length] = c;
    length++;
  }
}

int nextInt(Input *input) {
  char *line = readLine(input);
  int n = atoi(line);
  return n;
}

