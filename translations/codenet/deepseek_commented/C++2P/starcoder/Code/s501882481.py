
# Python 3 program to convert a string to a string representation.
def to_string(s):
    return s

# Python 3 program to convert a pair to a string representation.
def to_string(p):
    return "{" + to_string(p[0]) + "," + to_string(p[1]) + "}"

# Python 3 program to convert a vector to a string representation.
def to_string(v):
    ret = "{"
    for i in range(len(v) - 1):
        ret += to_string(v[i]) + ","
    if len(v) > 0:
        ret += to_string(v[-1])
    ret += "}"
    return ret

