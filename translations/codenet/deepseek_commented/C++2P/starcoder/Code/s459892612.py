# Declare three integer variables to store user input
a = int(input())
b = int(input())
c = int(input())

# Check if the sum of a and b is greater than or equal to c
if (a + b >= c):
  # If true, print "Yes"
  print("Yes")
else:
  # Otherwise, print "No"
  print("No")

# Return 0 to indicate successful execution
return 0

