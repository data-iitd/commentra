def solve():
    n = int(input())  # Read the input number
    print(int(not n))  # Output the boolean negation of n

if __name__ == "__main__":
    n = 1  # Initialize n with 1
    for _ in range(n):  # Run the solve function in a loop with n=1
        solve()  # Call the solve function

