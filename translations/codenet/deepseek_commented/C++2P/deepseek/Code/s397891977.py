import sys  # Step 1: Include necessary libraries for input/output operations

# Step 2: Define the main function
def main():
    r = int(sys.stdin.readline().strip())  # Step 3: Read an integer input from the user
    print(r * r)  # Step 4: Calculate the square of the input and output the result

# Step 5: Call the main function
if __name__ == "__main__":
    main()
