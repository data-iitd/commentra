// Step 1: Import necessary libraries for input/output operations
import java.util.Scanner;

public class Main {
  public static void main(String[] args) {
    // Step 2: Declare an integer variable to store the input
    int r;

    // Step 3: Read an integer input from the user
    Scanner sc = new Scanner(System.in);
    r = sc.nextInt();

    // Step 4: Calculate the square of the input and output the result
    System.out.println(r*r);
  }
}

