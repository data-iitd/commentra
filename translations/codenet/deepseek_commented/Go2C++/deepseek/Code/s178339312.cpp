#include <iostream>
using namespace std;

int main() {
    // Declare a variable to store the input integer
    int x;
    
    // Read an integer from the standard input and store it in x
    cin >> x;
    
    // Calculate the cube of x (x*x*x)
    int result = x * x * x;
    
    // Print the result
    cout << result << endl;
    
    return 0;
}

