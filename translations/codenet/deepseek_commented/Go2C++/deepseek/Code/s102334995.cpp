#include <iostream>
using namespace std;

int main() {
  // Declare variables a and b to store integers
  int a, b;
  
  // Read two integers from standard input and store them in a and b
  cin >> a >> b;
  
  // Print the product of a and b
  cout << a * b << endl;
  
  // Print the perimeter of a rectangle with sides a and b
  cout << (a + b) * 2 << endl;
  
  return 0;
}

