import java.util.*;

public class Main {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		int M = sc.nextInt();
		int ans = 0;
		if (N == 1 && M == 1) {
			ans += 1;
		} else if (1 < N && 1 < M) {
			ans += (N - 2) * (M - 2);
		} else {
			ans += (N * M) - 2;
		}
		System.out.println(ans);
	}
}

