import java.util.*;
import java.lang.*;
import java.io.*;
import java.math.*;

class Main {
    public static void main(String[] args) {
        //code
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] nums = new int[n];
        int last = 0;
        for(int i=0; i<n; i++){
            nums[i] = sc.nextInt();
        }
        int[] dp = new int[n+1];
        dp[0] = 1;
        Map<Integer, Integer> kek = new HashMap<>();
        Map<Integer, Integer> prev = new HashMap<>();
        for(int i=1; i<=n; i++){
            dp[i] = dp[i-1] + (prev.get(nums[i-1])!=i-1 ? kek.get(nums[i-1]):0);
            dp[i] %= 1000000007;
            kek.put(nums[i-1], dp[i]);
            prev.put(nums[i-1], i);
        }
        System.out.println(dp[n]);
    }
}
