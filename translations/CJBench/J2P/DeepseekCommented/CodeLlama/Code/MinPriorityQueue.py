import sys

class Main:
    def __init__(self, capacity):
        self.heap = [0] * (capacity + 1)
        self.capacity = capacity
        self.size = 0

    def is_empty(self):
        return self.size == 0

    def is_full(self):
        return self.size == self.capacity

    def insert(self, key):
        if self.is_full():
            raise Exception("Heap is full. Cannot insert new element.")
        self.size += 1
        self.heap[self.size] = key
        k = self.size
        while k > 1:
            if self.heap[k] < self.heap[k // 2]:
                self.heap[k], self.heap[k // 2] = self.heap[k // 2], self.heap[k]
            k = k // 2

    def peek(self):
        if self.is_empty():
            raise Exception("Heap is empty. Cannot peek.")
        return self.heap[1]

    def delete(self):
        if self.is_empty():
            raise Exception("Heap is empty. Cannot delete.")
        min = self.heap[1]
        self.heap[1] = self.heap[self.size]
        self.size -= 1
        self.sink()
        return min

    def sink(self):
        k = 1
        while 2 * k <= self.size:
            min_index = k
            if 2 * k <= self.size and self.heap[2 * k] < self.heap[min_index]:
                min_index = 2 * k
            if 2 * k + 1 <= self.size and self.heap[2 * k + 1] < self.heap[min_index]:
                min_index = 2 * k + 1
            if min_index == k:
                break
            self.heap[k], self.heap[min_index] = self.heap[min_index], self.heap[k]
            k = min_index

    def print(self):
        for i in range(1, self.size + 1):
            print(self.heap[i], end=" ")
        print()

    def heap_sort(self):
        for i in range(1, self.size + 1):
            self.delete()

    def main(self):
        capacity = int(sys.stdin.readline())
        queue = Main(capacity)
        while True:
            command = sys.stdin.readline().strip()
            try:
                if command == "insert":
                    value = int(sys.stdin.readline())
                    queue.insert(value)
                    print("Inserted:", value)
                elif command == "delete":
                    print("Deleted:", queue.delete())
                elif command == "peek":
                    print("Peek:", queue.peek())
                elif command == "print":
                    print("Heap:")
                    queue.print()
                elif command == "isEmpty":
                    print("Is Empty:", queue.is_empty())
                elif command == "exit":
                    print("Exiting...")
                    return
                else:
                    print("Unknown command:", command)
            except Exception as e:
                print("Error:", e)


