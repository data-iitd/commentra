def binarySearch(arr, target, row, colStart, colEnd):
    while colStart <= colEnd:
        midIndex = colStart + (colEnd - colStart) / 2
        if arr[row][midIndex] == target:
            return [row, midIndex]
        elif arr[row][midIndex] < target:
            colStart = midIndex + 1
        else:
            colEnd = midIndex - 1
    return [-1, -1]

